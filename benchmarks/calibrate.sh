#!/bin/bash
# Calibrate confidence threshold for pakka reviewer.
#
# Runs all 10 seeded-bug cases through reviewer+security agents at
# thresholds 70/75/80/85/90. Also runs 3 clean-diff negative cases.
# Writes results to benchmarks/calibration.md.
#
# Prerequisites: claude CLI available, pakka plugin installed.
# Usage: bash benchmarks/calibrate.sh
set -euo pipefail

SEEDS_DIR="benchmarks/seeds"
OUT="benchmarks/calibration.md"
RESULTS_DIR=".pakka/calibration"
mkdir -p "$RESULTS_DIR"

THRESHOLDS="70 75 80 85 90"
BUG_SEEDS="01-n-plus-1 02-null-deref 03-off-by-one 04-toctou 05-sql-injection 06-shell-injection 07-secret-literal 08-ignored-err 09-race 10-permission-escalation"
CLEAN_SEEDS="clean-01 clean-02 clean-03"

echo "pakka calibration — $(date -u +%Y-%m-%dT%H:%M:%SZ)"
echo ""

# --- Run each seed through the reviewer ---
for seed in $BUG_SEEDS $CLEAN_SEEDS; do
    seed_dir="$SEEDS_DIR/$seed"
    prompt=$(cat "$seed_dir/prompt.md")
    patch=$(cat "$seed_dir/seed.patch")
    result_file="$RESULTS_DIR/$seed.jsonl"

    echo "Running seed: $seed ..."

    # Feed the diff to claude in headless mode, asking for JSON-line findings
    claude -p "You are a code reviewer. $prompt

\`\`\`diff
$patch
\`\`\`

Output one JSON line per finding. Schema: {\"kind\":\"correctness|security\",\"file\":\"...\",\"line\":N,\"severity\":\"warn|error\",\"confidence\":N,\"rationale\":\"...\",\"fix\":\"...\"}
If no issues found, output nothing." > "$result_file" 2>/dev/null || true
done

# --- Score at each threshold ---
cat > "$OUT" << 'HEADER'
# Calibration Results

Generated by `benchmarks/calibrate.sh`.

## Catch rate by threshold

| Seed | Bug class | 70 | 75 | 80 | 85 | 90 |
|------|-----------|----|----|----|----|-----|
HEADER

for seed in $BUG_SEEDS; do
    result_file="$RESULTS_DIR/$seed.jsonl"
    expected_file="$SEEDS_DIR/$seed/expected.json"
    expected_kind=$(python3 -c "import json; print(json.load(open('$expected_file'))['kind'])" 2>/dev/null || echo "unknown")
    bug_class=$(python3 -c "import json; print(json.load(open('$expected_file'))['bug_class'])" 2>/dev/null || echo "unknown")

    row="| $seed | $bug_class |"
    for threshold in $THRESHOLDS; do
        # Check if any finding matches expected kind with confidence >= threshold
        caught=$(python3 -c "
import json, sys
try:
    findings = [json.loads(l) for l in open('$result_file') if l.strip() and l.strip()[0]=='{']
    match = any(f.get('confidence',0) >= $threshold and f.get('kind','') == '$expected_kind' for f in findings)
    print('Y' if match else 'N')
except: print('?')
" 2>/dev/null || echo "?")
        row="$row $caught |"
    done
    echo "$row" >> "$OUT"
done

# --- False positives on clean seeds ---
echo "" >> "$OUT"
echo "## False positives on clean diffs" >> "$OUT"
echo "" >> "$OUT"
echo "| Seed | Findings at 80 | False positive? |" >> "$OUT"
echo "|------|---------------|-----------------|" >> "$OUT"

fp_count=0
for seed in $CLEAN_SEEDS; do
    result_file="$RESULTS_DIR/$seed.jsonl"
    count=$(python3 -c "
import json
try:
    findings = [json.loads(l) for l in open('$result_file') if l.strip() and l.strip()[0]=='{']
    above = [f for f in findings if f.get('confidence',0) >= 80 and f.get('severity','') == 'error']
    print(len(above))
except: print(0)
" 2>/dev/null || echo "0")
    is_fp="no"
    if [ "$count" -gt 0 ]; then
        is_fp="YES"
        fp_count=$((fp_count + 1))
    fi
    echo "| $seed | $count | $is_fp |" >> "$OUT"
done

# --- Summary ---
echo "" >> "$OUT"
echo "## Summary" >> "$OUT"
echo "" >> "$OUT"

for threshold in $THRESHOLDS; do
    catches=0
    for seed in $BUG_SEEDS; do
        result_file="$RESULTS_DIR/$seed.jsonl"
        expected_file="$SEEDS_DIR/$seed/expected.json"
        expected_kind=$(python3 -c "import json; print(json.load(open('$expected_file'))['kind'])" 2>/dev/null || echo "unknown")
        caught=$(python3 -c "
import json
try:
    findings = [json.loads(l) for l in open('$result_file') if l.strip() and l.strip()[0]=='{']
    match = any(f.get('confidence',0) >= $threshold and f.get('kind','') == '$expected_kind' for f in findings)
    print(1 if match else 0)
except: print(0)
" 2>/dev/null || echo "0")
        catches=$((catches + caught))
    done
    echo "- Threshold $threshold: $catches/10 catches, $fp_count FP on clean corpus" >> "$OUT"
done

echo "" >> "$OUT"
echo "Chosen threshold: **80** (update settings.json if different)" >> "$OUT"

echo ""
echo "Calibration complete. Results in $OUT"
cat "$OUT"
